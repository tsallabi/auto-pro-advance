const lines = data.split('\\n');
const results = [];
let i = 0;
while (i < lines.length) {
    if (lines[i].trim() === '') {
        i++;
        continue;
    }
    // Title is skipped, we care about the 10 fields following
    // 0: title
    // 1: condition
    // 2: make
    // 3: model
    // 4: year
    // 5: transmission
    // 6: fuel
    // 7: mileage
    // 8: price
    // 9: city
    // 10: source (skip)
    const condition = lines[i + 1];
    const make = lines[i + 2];
    const model = lines[i + 3];
    const year = parseInt(lines[i + 4], 10);
    const transmission = lines[i + 5];
    const fuel = lines[i + 6];
    const mileage = lines[i + 7];
    const price = lines[i + 8];
    const city = lines[i + 9];

    if (make && model) {
        results.push({
            make: make.trim(),
            makeEn: make.trim(), // We'll just use the Arabic make for English as fallback unless mapped
            model: model.trim(),
            modelEn: model.trim(),
            year: year,
            condition: condition ? condition.trim() : '',
            transmission: transmission ? transmission.trim() : '',
            fuel: fuel ? fuel.trim() : '',
            mileage: mileage ? mileage.trim() : '',
            price: price ? price.trim() : '',
            city: city ? city.trim() : ''
        });
    }

    i += 11;
}

fs.writeFileSync('parsed_estimates.json', JSON.stringify(results, null, 2));
console.log('Parsed ' + results.length + ' estimates.');
